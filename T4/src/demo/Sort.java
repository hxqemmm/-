package demo;

public class Sort {
	private int work;
	private String name;
	
	public Sort(String name,int work) {
		this.name=name;
		this.work=work;
	}

	public int getWork() {
		return work;
	}

	public void setWork(int work) {
		this.work = work;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	
}
